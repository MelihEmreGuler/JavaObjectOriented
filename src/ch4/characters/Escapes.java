package ch4.characters;

public class Escapes {
    public static void main(String[] args) {

        System.out.println("Backspace: " + "Hello\bWorld");
        System.out.println("Tab: " + "Hello\tWorld");
        System.out.println("Linefeed: " + "Hello\nWorld");
        System.out.println("Carriage Return: " + "Hello\rWorld");
        System.out.println("Formfeed: " + "Hello\fWorld");
        System.out.println("Double Quote: " + "Hello\"World");
        System.out.println("Single Quote: " + "Hello\'World");
        System.out.println("Backslash: " + "Hello\\World");
    }
}
